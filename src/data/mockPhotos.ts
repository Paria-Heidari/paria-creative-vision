// Helper functions
export const getPhotosByCategory = (category?: string, subcategory?: string): Photo[] => {
  if (!category || category === 'all') return mockPhotos;

  let filtered = mockPhotos.filter((p) => p.category === category);

  if (subcategory) {
    filtered = filtered.filter((p) => p.subcategory === subcategory);
  }

  return filtered;
};

export const getFeaturedPhotos = (limit: number = 6): Photo[] => {
  return mockPhotos.filter((p) => p.featured).slice(0, limit);
};
